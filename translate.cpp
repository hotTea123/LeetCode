#include<string>
#include<iostream>
using namespace std;

//[[47,220],[65,1],[98,113],[126,196],[186,218],[320,205],[686,317],[707,325],[754,104],[781,105]]
void translate(string& s){
    for(int i=0;i<s.size();i++){
        if(s[i] == '[')
            s[i] = '{';
        else if(s[i] == ']')
            s[i] = '}';
    }
}


int main(){
    string s = "[[47,50],[1,10],[27,36],[40,47],[20,27],[15,23],[10,18],[27,36],[17,25],[8,17],[24,33],[23,28],[21,27],[47,50],[14,21],[26,32],[16,21],[2,7],[24,33],[6,13],[44,50],[33,39],[30,36],[6,15],[21,27],[49,50],[38,45],[4,12],[46,50],[13,21]]";

    translate(s);
    for(int i=0;i<s.size();i++){
        cout<<s[i];
    }
    cout<<endl;
    return 0;
}