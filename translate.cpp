#include<bits/stdc++.h>
using namespace std;

void translate(string& s){
    for(int i=0;i<s.size();i++){
        if(s[i] == '[')
            s[i] = '{';
        else if(s[i] == ']')
            s[i] = '}';
    }
}


int main(){
    string s = "[[4,9,1],[2,5,1],[7,9,0],[6,10,0],[1,7,1],[0,7,1],[0,3,0],[3,3,1],[7,9,0],[7,7,0],[6,7,0],[7,9,0],[3,9,0],[10,10,1],[8,9,0],[6,9,1],[1,2,1],[3,9,0],[8,10,1],[4,7,1],[9,10,1],[8,9,0],[5,8,0],[8,9,1],[0,7,1],[2,2,1],[8,8,1],[3,7,0],[1,10,1],[9,9,1],[4,9,0],[5,6,1],[7,8,1],[8,9,0],[7,7,1],[9,9,1]]";

    translate(s);
    for(int i=0;i<s.size();i++){
        cout<<s[i];
    }
    cout<<endl;
    return 0;
}